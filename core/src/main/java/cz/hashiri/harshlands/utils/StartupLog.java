    public static void log(@Nonnull String message) {
        long now = System.nanoTime();
        long elapsedMs;
        synchronized (LOCK) {
            elapsedMs = (now - lastLogNanos) / 1_000_000L;
            lastLogNanos = now;
        }

        send("&f[&5Harshlands&f] &f" + message + " &8(" + elapsedMs + " ms)");
    }

    private static void send(@Nonnull String line) {
        Bukkit.getConsoleSender().sendMessage(ChatColor.translateAlternateColorCodes('&', line));
    }

    private static void sendBanner(@Nonnull String line) {
        // Full blocks look much brighter in many console renderers; shaded blocks keep the same hue but darker output.
        send(BANNER_COLOR + " " + line.replace('\u2588', '\u2593'));
    }
